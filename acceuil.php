
<div class="pre">
     Le projet que nous avons réalisé est une application web de gestion des bulletins de notes destinée aux étudiants de la L1 IRT. Ce système permet de gérer les étudiants, les matières, les notes et de générer les bulletins de façon dynamique. Grâce à une interface simple et des fonctionnalités de type CRUD (Créer, Lire, Mettre à jour, Supprimer), chaque acteur peut facilement interagir avec la base de données. Avant de vous présenter une démonstration du fonctionnement du système, découvrons d’abord les membres du groupe ainsi que le rôle que chacun a joué dans la réalisation de ce projet.

